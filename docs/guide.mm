## Ruby Text (Furigana)

**Syntax:**

````
{kanji}(reading)
{Tokyo}(Tokyo)
````

**Display:**

{kanji}(reading)

{Tokyo}(Tokyo)

---

## Details Block

**Syntax:**

````
===Click to expand
Content inside the details block.
Can contain multiple paragraphs.
===
````

**Display:**

===Click to expand
Content inside the details block.
Can contain multiple paragraphs.
===

---

## Admonition Block

**Syntax:**

````
:::note info
This is an information message.
:::

:::note warn
This is a warning message.
:::

:::note alert
This is a critical warning.
:::
````

**Display:**

:::note info
This is an information message.
:::

:::note warn
This is a warning message.
:::

:::note alert
This is a critical warning.
:::

---

## Footnotes

**Syntax:**

````
Text with footnote[^1]

[^1]: Footnote content
````

**Display:**

Text with footnote[^1]

[^1]: Footnote content

