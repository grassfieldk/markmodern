# Markmodern Syntax Guide

A comprehensive guide to all syntax features and their display examples.

---

## Headings

**Syntax:**

````
# h1
## h2
### h3
#### h4
##### h5
###### h6
````

**Display:**

# h1
## h2
### h3
#### h4
##### h5
###### h6

---

## Text Formatting

**Syntax:**

````
**bold**
*italic*
***bold and italic***
~~strikethrough~~
`code`
````

**Display:**

**bold**

*italic*

***bold and italic***

~~strikethrough~~

`code`

---

## Links

**Syntax:**

````
[link](https://example.com)
````

**Display:**

[link](https://example.com)

---

## Images

**Syntax:**

````
![alt text](https://via.placeholder.com/300x200)
-![with caption](https://via.placeholder.com/400x300)
````

**Display:**

![alt text](https://via.placeholder.com/300x200)

-![with caption](https://via.placeholder.com/400x300)

---

## Lists

**Syntax:**

````
- item 1
- item 2
  - nested 1
  - nested 2
- item 3
````

**Display:**

- item 1
- item 2
  - nested 1
  - nested 2
- item 3

**Ordered:**

````
1. first
2. second
   1. nested
3. third
````

**Display:**

1. first
2. second
   1. nested
3. third

**Checkboxes:**

````
- [x] completed
- [ ] incomplete
````

**Display:**

- [x] completed
- [ ] incomplete

---

